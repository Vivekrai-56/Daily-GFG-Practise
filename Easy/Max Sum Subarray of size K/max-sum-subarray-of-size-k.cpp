class Solution{   
public:
    long maximumSumSubarray(int k, vector<int> &arr , int n){
        // code here 
        long curr =0;
        for(int i=0;i<k;i++)
        {
            curr += arr[i];
        }
        long res = curr;
        for(int i=k;i<n;i++)
        {
            curr = curr+ arr[i]-arr[i-k];
            res = max(res,curr);
        }
        return res;
    }
};

